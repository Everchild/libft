#include <libft.h>

void				free_tab(char ***s)
{
	size_t			i;

	i = 0;
	if (s)
	{
		if (*s)
		{
			while ((*s)[i])
			{
				ft_strdel(&((*s)[i]));
				(*s)[i] = NULL;
				i++;
			}
			ft_memdel((void **)*s);
			**s = NULL;
		}
	}
}
